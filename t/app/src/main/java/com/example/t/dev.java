package com.example.t;

public class dev{
    private String st;
    private int pr;

    public dev(String st, int pr) {
        this.st = st;
        this.pr = pr;
    }

    public String getSt() {
        return st;
    }

    public void setSt(String st) {
        this.st = st;
    }

    public int getPr() {
        return pr;
    }

    public void setPr(int pr) {
        this.pr = pr;
    }
}
